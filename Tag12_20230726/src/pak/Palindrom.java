public class Palindrom {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		System.out.print("Num: ");
		int[] num = new int[] {scan.nextInt()};
		
		for (int i = 0; i < num.length; i++) {
			System.out.println(num[i]);
		}
		
		
	}
}
